<?php

namespace App\Entity;



class Newsletter
{
    protected $name;
    protected $email;

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(string $name): self
    {
        $this->name = $name;

        return $this;
    }

    public function getEmail(): ?string
    {
        return $this->email;
    }

    public function setEmail(string $email): self
    {
        $this->name = $email;

        return $this;
    }
}